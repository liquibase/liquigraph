package org.liquigraph.spring.starter;

import org.springframework.beans.factory.annotation.Qualifier;

import java.lang.annotation.Documented;
import java.lang.annotation.Retention;
import java.lang.annotation.Target;

import static java.lang.annotation.ElementType.ANNOTATION_TYPE;
import static java.lang.annotation.ElementType.FIELD;
import static java.lang.annotation.ElementType.METHOD;
import static java.lang.annotation.ElementType.PARAMETER;
import static java.lang.annotation.ElementType.TYPE;
import static java.lang.annotation.RetentionPolicy.RUNTIME;

/**
 * Qualifier annotation for a DataSource to be injected in to Liquigraph. If used for a
 * second data source, the other (main) one would normally be marked as {@code @Primary}.
 *
 * @author Michael Vitz
 */
@Target({ FIELD, METHOD, PARAMETER, TYPE, ANNOTATION_TYPE })
@Retention(RUNTIME)
@Documented
@Qualifier
public @interface LiquigraphDataSource {
}
